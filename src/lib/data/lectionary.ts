// ── Internal types ────────────────────────────────────────────────────────────

type Seg = readonly [number, number, number, number]; // [cStart, vStart, cEnd, vEnd]

interface R {
  book: string;   // USFM book code matching build-db.ts BOOKS list
  cite: string;   // human-readable citation
  segs: Seg[];
}

interface Day {
  first: R;
  psalm: R;
  second?: R;   // omitted for weekdays
  gospel: R;
}

interface Entry {
  A?: Day;
  B?: Day;
  C?: Day;
  common?: Day; // same for all three years
}

// ── Helpers ───────────────────────────────────────────────────────────────────

/** Single continuous range (possibly cross-chapter) */
function r(book: string, cite: string, cs: number, vs: number, ce: number, ve: number): R {
  return { book, cite, segs: [[cs, vs, ce, ve]] };
}

/** Multiple discontinuous segments (verses omitted mid-passage) */
function rm(book: string, cite: string, ...segs: Seg[]): R {
  return { book, cite, segs: [...segs] };
}

